public class Questao06 {
    public static void main(String[] args) {
        int n1 = 10;
        int n2 = 5;

        System.out.println("A soma de 10 + 5 é " + (n1 + n2));
        System.out.println("A diferença entre 10 e 5 é " + (n1 - n2));
        System.out.println("O produto de 10 e 5 é " + (n1 * n2));
        System.out.println("O quociente de 10 e 5 é " + (n1 / n2));
    }
}
